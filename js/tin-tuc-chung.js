const defaultImage = "../img/default-news.jpg";

// Render News List
function renderNewsList(listData = newsData.list) {
  const container = document.getElementById("newsListContainer");

  if (!container) return;

  container.innerHTML = "";

  listData.forEach((item) => {
    const imageSrc =
      item.image && item.image.trim() !== "" ? item.image : defaultImage;

    const newsItem = document.createElement("div");
    newsItem.className = "news-list-item-general";
    newsItem.addEventListener("click", () => {
      window.location.href = `chi-tiet-tin-tuc.html`;
    });

    const unreadTitle = item.unread ? "unread" : "";
    const unreadClass = item.unread ? "unseen" : "";
    const newBadge = item.unread
      ? '<div class="news-icon-unseen">NEW</div>'
      : "";

    newsItem.innerHTML = `
      <img class="news-list-item-icon" 
        src="${imageSrc}"
        onerror="this.onerror=null; this.src='${defaultImage}';"></img>
      <div class="news-list-item-content">
        <div class="news-list-item-title ${unreadTitle}">${item.title}</div>
        <div class="news-list-item-bottom ${unreadClass}">
          ${newBadge}
          <div class="news-list-item-date">${item.date}</div>
        </div>
        <div class="news-description">${item.description}</div>
      </div>
    `;

    container.appendChild(newsItem);
  });
}

